import numpy as np
# This script demonstrates basic arithmetic operations using NumPy arrays.
# Arithmetic operations
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # Element-wise multiplication
print(a / b)  # Division
print(a ** 2) # Power operation